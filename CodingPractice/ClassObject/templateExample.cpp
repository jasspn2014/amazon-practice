#include <iostream>
#define MAX 100000
using namespace std;

void solve()
{
    int n,money = 0;
    cin>>n;
    short int arr[n];
    for(int i=0;i<n;i++)
        cin>>arr[i];
    bool canSell = true;
    for(int i=0;i<n;i++)
    {
        if(arr[i] == 5)
        {
            money += 5;
        }

        else if(arr[i]-5 <= money)
        {
            money += 5;
            money -= (arr[i]-5);
        }
        else{
            canSell = false;
            break;
        }
    }

    if(canSell)
        cout<<"YES"<<endl;
    else
        cout<<"NO"<<endl;


}

int main() {
    int t;
    cin>>t;
    while(t--)
    {
        solve();
    }
    return 0;
}





























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































